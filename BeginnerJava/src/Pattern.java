
public class Pattern 
{
	public static void main(String args[])
	{
		for(int i=1;i<=4;i++)
		{
			System.out.print("* ");
		}
		System.out.println();

		for(int i=1;i<=2;i++)
		{
			if(i==2)
			{
				System.out.print("  ");
				System.out.print("  ");
			}
			System.out.print("* ");
		}
		System.out.println();
		for(int i=1;i<=2;i++)
		{
			if(i==2)
			{
				System.out.print("  ");
				System.out.print("  ");
			}
			System.out.print("* ");
		}
		System.out.println();
		for(int i=1; i<=4; i++)
		{
			System.out.print("* ");
		}

	}

}
